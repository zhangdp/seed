    /**
     * 根据文件名后缀获取文件类型
     *
     * @param ext
     * @return
     */
    public static String getType(String ext) {
        return TYPES.get(ext != null ? ext.trim().toLowerCase() : null);
    }

    /**
     * 根据文件名后缀获取文件类型，如果不存在默认返回application/octet-stream
     *
     * @param ext
     * @return
     */
    public static String getTypeOrDefault(String ext) {
        return getType(ext, DEFAULT_TYPE);
    }

    /**
     * 根据文件名后缀获取文件类型，如果不存在则返回指定默认值
     *
     * @param ext
     * @param defaultType
     * @return
     */
    public static String getType(String ext, String defaultType) {
        return TYPES.getOrDefault(ext != null ? ext.trim().toLowerCase() : null, defaultType);
    }

    /**
     * 新增类型
     *
     * @param key
     * @param value
     * @return
     */
    public static String addType(String key, String value) {
        return TYPES.put(key, value);
    }
